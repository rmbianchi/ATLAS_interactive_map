	// Check and fix collisions and overlaps between institutes
	// idea taken from:
	// http://blog.safaribooksonline.com/2014/03/11/solving-d3-label-placement-constraint-relaxing/
	// http://jsfiddle.net/thudfactor/HdwTH/
